    private void ListarFuncionario() {
        try {
            FuncionarioDAO objFuncionarioDAO = new FuncionarioDAO();
            DefaultTableModel model = (DefaultTableModel) TBFuncionario.getModel();
            model.setNumRows(0);
            ArrayList<FuncionarioDTO> lista = objFuncionarioDAO.pesquisarFuncionario();
            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getCodigo(),
                    lista.get(num).getCpf(),
                    lista.get(num).getNome(),
                    lista.get(num).getFone(),
                    lista.get(num).getEmail(),
                    lista.get(num).getSalario()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "FrmPesquisar ListarFuncionario\n" + e);
        }
    }

    private void CarregarCamposFuncionario() {
        int setar = TBFuncionario.getSelectedRow();
        if (setar < 0) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário na tabela!");
            return;
        }
        frmfuncionario.txtid.setText(valorCelula(setar, 0));
        frmfuncionario.txtcpf.setText(valorCelula(setar, 1));
        frmfuncionario.txtnome.setText(valorCelula(setar, 2));
        frmfuncionario.txtfone.setText(valorCelula(setar, 3));
        frmfuncionario.txtemail.setText(valorCelula(setar, 4));
        frmfuncionario.txtsalario.setText(valorCelula(setar, 5));
        this.dispose();
    }

    private String valorCelula(int linha, int coluna) {
        Object valor = TBFuncionario.getModel().getValueAt(linha, coluna);
        return valor == null ? "" : valor.toString();
    }
