    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um produto antes de alterar!");
            return;
        }
        ProdutoDAO objProdutoDAO = new ProdutoDAO();
        objProdutoDAO.alterarProduto(montarProdutoDTO());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um produto antes de excluir!");
            return;
        }
        ProdutoDAO objProdutoDAO = new ProdutoDAO();
        objProdutoDAO.excluirProduto(montarProdutoDTO());
    }//GEN-LAST:event_btnExcluirActionPerformed

