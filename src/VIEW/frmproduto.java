    private ProdutoDTO montarProdutoDTO() {
        ProdutoDTO objProdutoDTO = new ProdutoDTO();
        if (!txtid.getText().isEmpty()) {
            objProdutoDTO.setCodigo(Integer.parseInt(txtid.getText()));
        }
        objProdutoDTO.setDescricao(txtdescricao.getText());
        try {
            objProdutoDTO.setEstoque(Integer.parseInt(txtestoque.getText()));
        } catch (NumberFormatException e) {
            objProdutoDTO.setEstoque(0);
        }
        objProdutoDTO.setStatus(cbstatus.getSelectedItem().toString());
        try {
            objProdutoDTO.setPreco(new BigDecimal(txtpreco.getText().replace(",", ".")));
        } catch (NumberFormatException e) {
            objProdutoDTO.setPreco(BigDecimal.ZERO);
        }
        try {
            objProdutoDTO.setCodigoCategoria(Integer.parseInt(txtcategoria.getText()));
        } catch (NumberFormatException e) {
            objProdutoDTO.setCodigoCategoria(0);
        }
        return objProdutoDTO;
    }

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        ProdutoDAO objProdutoDAO = new ProdutoDAO();
        objProdutoDAO.cadastrarProduto(montarProdutoDTO());
    }//GEN-LAST:event_btnOKActionPerformed

