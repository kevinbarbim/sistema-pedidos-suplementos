    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um entregador antes de alterar!");
            return;
        }
        EntregadorDAO objEntregadorDAO = new EntregadorDAO();
        objEntregadorDAO.alterarEntregador(montarEntregadorDTO());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um entregador antes de excluir!");
            return;
        }
        EntregadorDAO objEntregadorDAO = new EntregadorDAO();
        objEntregadorDAO.excluirEntregador(montarEntregadorDTO());
    }//GEN-LAST:event_btnExcluirActionPerformed

