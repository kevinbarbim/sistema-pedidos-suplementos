    private EntregadorDTO montarEntregadorDTO() {
        EntregadorDTO objEntregadorDTO = new EntregadorDTO();
        if (!txtid.getText().isEmpty()) {
            objEntregadorDTO.setCodigo(Integer.parseInt(txtid.getText()));
        }
        objEntregadorDTO.setNome(txtnome.getText());
        objEntregadorDTO.setCpf(txtcpf.getText());
        objEntregadorDTO.setFone(txtfone.getText());
        objEntregadorDTO.setEmail(txtemail.getText());
        objEntregadorDTO.setObs(txtobs.getText());
        return objEntregadorDTO;
    }

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        EntregadorDAO objEntregadorDAO = new EntregadorDAO();
        objEntregadorDAO.cadastrarEntregador(montarEntregadorDTO());
    }//GEN-LAST:event_btnOKActionPerformed

