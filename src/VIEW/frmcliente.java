    private ClienteDTO montarClienteDTO() {
        ClienteDTO objClienteDTO = new ClienteDTO();
        if (!txtid.getText().isEmpty()) {
            objClienteDTO.setCodigo(Integer.parseInt(txtid.getText()));
        }
        objClienteDTO.setNome(txtnome.getText());
        objClienteDTO.setCpf(txtcpf.getText());
        objClienteDTO.setFone(txtfone.getText());
        objClienteDTO.setEmail(txtemail.getText());
        objClienteDTO.setStatus(cbstatus.getSelectedItem().toString());

        EnderecoDTO objEnderecoDTO = new EnderecoDTO();
        objEnderecoDTO.setCep(txtcep.getText());
        objEnderecoDTO.setCidade(txtcidade.getText());
        objEnderecoDTO.setEstado(txtestado.getText());
        objEnderecoDTO.setStatus(txtstatusendereco.getText());
        objClienteDTO.setEndereco(objEnderecoDTO);

        return objClienteDTO;
    }

    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.cadastrarCliente(montarClienteDTO());
    }//GEN-LAST:event_btnOKActionPerformed

