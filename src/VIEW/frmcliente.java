    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um cliente antes de alterar!");
            return;
        }
        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.alterarCliente(montarClienteDTO());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (txtid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pesquise um cliente antes de excluir!");
            return;
        }
        ClienteDAO objClienteDAO = new ClienteDAO();
        objClienteDAO.excluirCliente(montarClienteDTO());
    }//GEN-LAST:event_btnExcluirActionPerformed

